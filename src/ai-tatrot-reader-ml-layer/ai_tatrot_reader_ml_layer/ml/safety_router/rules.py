import re



# Вспомогательные функции
def match_any(text: str, patterns: list[str]) -> bool:
    """Возвращает True если хотя бы один паттерн совпадает с текстом."""
    lowered = text.lower()
    return any(re.search(p, lowered, re.IGNORECASE) for p in patterns)


# Идиомы — байпас всех уровней
IDIOMS_ALLOWLIST: list[str] = [
    r"убить\s+время",
    r"убить\s+двух\s+зайц",
    r"умереть\s+со?\s+смеху?",
    r"умереть\s+от\s+смеха",
    r"хочу\s+умереть\s+от\s+смеха",
    r"умру\s+от\s+смеха",
    r"до\s+смерти\s+(хочу|люблю|устал)",
    r"смерть\s+как\s+хочется",
    r"убиваюсь\s+со\s+смеху",
    r"как\s+убить\s+время",
    r"чтоб\s+убить\s+время",
]


def is_idiom(text: str) -> bool:
    return match_any(text, IDIOMS_ALLOWLIST)



