"""
Правила первого уровня Safety Router.

Содержит:
- Идиомы-allowlist (не блокируем)
- HARD_BLOCK паттерны (суицид, насилие, наркотики, политика/война)
- SOFT_BLOCK паттерны (смерть буквально, заговоры, мелкий криминал)
- Словарь мягкого раздражения → toxic_safe
"""

import re


# ---------------------------------------------------------------------------
# Вспомогательная функция
# ---------------------------------------------------------------------------

def _match_any(text: str, patterns: list[str]) -> bool:
    t = text.lower()
    return any(re.search(p, t, re.IGNORECASE) for p in patterns)


# ---------------------------------------------------------------------------
# Идиомы — пропускаем без блокировки
# ---------------------------------------------------------------------------

_IDIOMS = [
    r"убить\s+время",
    r"убить\s+двух\s+зайц",
    r"умереть\s+со?\s+смеху?",
    r"умереть\s+от\s+смеха",
    r"хочу\s+умереть\s+от\s+смеха",
    r"умру\s+от\s+смеха",
    r"до\s+смерти\s+(хочу|люблю|устал)",
    r"смерть\s+как\s+хочется",
    r"убиваюсь\s+со\s+смеху",
    r"как\s+убить\s+время",
    r"чтоб\s+убить\s+время",
]


def is_idiom(text: str) -> bool:
    return _match_any(text, _IDIOMS)


