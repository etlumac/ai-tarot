# Allowlist для death_general — эти вопросы про смерть таролог принимает
_DEATH_ALLOWLIST = [
    r"пережить\s+смерть",
    r"смерть\s+близк",
    r"потеря\s+близк",
    r"\bгоре\b",
    r"скорб\w+",
    r"прощани[ея]",
    r"проститься",
]


def _is_death_allowlist(text: str) -> bool:
    return _match_any(text, _DEATH_ALLOWLIST)


# ---------------------------------------------------------------------------
# Словарь мягкого раздражения → toxic_safe
# (бытовое недовольство, которое таролог принимает с эмпатией)
# ---------------------------------------------------------------------------

_MILD_FRUSTRATION = [
    r"(все|всё|всех)\s+вокруг\s+(раздраж|бесит|надоел)",
    r"все\s+(раздраж|бесят|надоели)",
    r"всё\s+(надоело|бесит|раздражает)",
    r"(ненавижу|ненавидеть)\s+(свою?\s+)?(жизнь|себя|всё|всех)",
    r"(достало|задолбало|надоело)\s+всё",
    r"(почему\s+)?(мне|у\s+меня)\s+всё\s+(плохо|не\s+так|не\s+везёт)",
    r"(устал[аи]?|вымотал[аи]?)\s+от\s+всего",
    r"всё\s+раздражает",
    r"(шеф|начальник|босс|коллег\w+).{0,20}(невынос|бесит|ужасн|идиот)",
    r"почему\s+(мой|все).{0,20}(так\s+себя\s+ведут|такие\s+)",
]


def is_mild_frustration(text: str) -> bool:
    return _match_any(text, _MILD_FRUSTRATION)


# ---------------------------------------------------------------------------
# Публичная функция: rule_based_safety
# ---------------------------------------------------------------------------

def rule_based_safety(text: str) -> tuple[str, str | None]:
    """
    Применяет правила первого уровня.

    Returns:
        ("hard_block", category) | ("soft_block", category) | ("safe", None)
    """
    if is_idiom(text):
        return "safe", None

    t = text.lower()

    # Жёсткая блокировка
    for category, patterns in HARD_BLOCK.items():
        if _match_any(t, patterns):
            return "hard_block", category

    # Мягкая блокировка
    for category, patterns in SOFT_BLOCK.items():
        if _match_any(t, patterns):
            # death_general с allowlist — пропускаем
            if category == "death_general" and _is_death_allowlist(t):
                continue
            return "soft_block", category

    return "safe", None